public class Student {
    public String x;
    public int y;
    public int z;

    public Student(String x, int y, int z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }

    public String getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public int getZ() {
        return z;
    }
}
